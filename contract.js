async function mintNFT(signer, userName, courseName, organization, completionDate) {
    const nftContract = new ethers.Contract(contractAddress, contractABI, signer);
    try {
        const transactionResponse = await nftContract.mintCertificate(signer.getAddress(), userName, courseName, organization, completionDate);
        await transactionResponse.wait();
        alert("NFT Minted!");
        console.log("NFT Minted!");
    } catch (error) {
        console.error("Error minting NFT:", error);
    }
}

