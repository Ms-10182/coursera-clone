async function resetCertificate() {
    // Assume user's connected wallet address is to be used
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const signer = provider.getSigner();
    const addressToReset = await signer.getAddress(); // Getting the signer's address
    const contract = new ethers.Contract(contractAddress, contractABI, signer);

    try {
        const tx = await contract.resetCertificateStatus(addressToReset); // Passing the address
        await tx.wait(); // Wait for the transaction to be mined
        console.log("Certificate reset successfully for address:", addressToReset);
        alert("Certificate reset successfully!");
    } catch (error) {
        console.error(error);
        alert("Failed to reset certificate for address: " + addressToReset);
    }
}


